package cy.olesiabokk.stateapp.entity;

public class Citizen {
    private int id;
    private String name;
    private String surname;
    private int age;
    private State state;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }

    public State getState() {
        return state;
    }


}
